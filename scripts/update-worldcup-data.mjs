import { writeFileSync } from 'node:fs';

const LIVE_BASE = process.env.WORLDCUP_API_BASE || 'https://worldcup26.ir/get';
const TIMEOUT_MS = Number(process.env.WORLDCUP_API_TIMEOUT_MS || 12000);

async function fetchJson(url) {
  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), TIMEOUT_MS);
  try {
    const response = await fetch(url, { signal: controller.signal, headers: { accept: 'application/json' } });
    if (!response.ok) throw new Error(`${response.status} ${response.statusText}`);
    return await response.json();
  } finally {
    clearTimeout(timer);
  }
}

function normalizeTeam(item) {
  return {
    id: Number(item.id),
    name: item.name_en || item.name || item.team || 'TBD',
    fifaCode: item.fifa_code || item.fifaCode || 'TBD',
    iso2: item.iso2 || 'TBD',
    group: item.groups || item.group || '',
    flag: item.flag || '',
  };
}

function normalizeStadium(item) {
  return {
    id: Number(item.id),
    name: item.name_en || item.name || item.fifa_name || '',
    city: item.city_en || item.city || '',
    country: item.country_en || item.country || '',
    capacity: Number(item.capacity || 0),
  };
}

function normalizeMatch(item, teamById, stadiumById) {
  const home = teamById.get(Number(item.home_team_id)) || teamById.get(Number(item.homeTeam?.id));
  const away = teamById.get(Number(item.away_team_id)) || teamById.get(Number(item.visitingTeam?.id));
  const stadium = stadiumById.get(Number(item.stadium_id)) || stadiumById.get(Number(item.stadium?.id));
  return {
    id: Number(item.id),
    type: item.type || 'group',
    group: item.group || '',
    matchday: Number(item.matchday || 0),
    localDate: item.local_date || item.localDate || '',
    date: item.date || '',
    home: home?.fifaCode || item.home || 'TBD',
    away: away?.fifaCode || item.away || 'TBD',
    homeName: home?.name || item.homeName || 'TBD',
    awayName: away?.name || item.awayName || 'TBD',
    stadium: stadium?.name || item.stadium || '',
    city: stadium?.city || item.city || '',
    country: stadium?.country || item.country || '',
    source: 'worldcup26.ir live API',
    finished: String(item.finished || '').toLowerCase() === 'true',
    homeScore: Number.isFinite(Number(item.home_score)) ? Number(item.home_score) : undefined,
    awayScore: Number.isFinite(Number(item.away_score)) ? Number(item.away_score) : undefined,
    elapsed: item.time_elapsed || item.match_minute || '',
  };
}

function tsFile({ teams, stadiums, groups, matches, source }) {
  return `// Generated by scripts/update-worldcup-data.mjs\n// Last update: ${new Date().toISOString()}\n\nexport type WorldCupTeam = { id: number; name: string; fifaCode: string; iso2: string; group: string; flag: string };\nexport type WorldCupStadium = { id: number; name: string; city: string; country: string; capacity: number };\nexport type WorldCupMatch = { id: number; type: string; group: string; matchday: number; localDate: string; date: string; home: string; away: string; homeName: string; awayName: string; stadium: string; city: string; country: string; source: string; finished?: boolean; homeScore?: number; awayScore?: number; elapsed?: string };\nexport type WorldCupGroup = { group: string; teams: string[] };\n\nexport const worldCupSource = ${JSON.stringify(source, null, 2)} as const;\n\nexport const worldCupTeams: WorldCupTeam[] = ${JSON.stringify(teams, null, 2)};\n\nexport const worldCupStadiums: WorldCupStadium[] = ${JSON.stringify(stadiums, null, 2)};\n\nexport const worldCupGroups: WorldCupGroup[] = ${JSON.stringify(groups, null, 2)};\n\nexport const worldCupMatches: WorldCupMatch[] = ${JSON.stringify(matches, null, 2)};\n\nexport const tournamentStats = {\n  teams: worldCupTeams.length,\n  groups: worldCupGroups.length,\n  matches: worldCupMatches.length,\n  stadiums: worldCupStadiums.length,\n  groupMatches: worldCupMatches.filter((match) => match.type === 'group').length,\n  knockoutMatches: worldCupMatches.filter((match) => match.type !== 'group').length,\n};\n`;
}

async function main() {
  let liveError = '';
  let teams = [];
  let stadiums = [];
  let matches = [];
  try {
    const [teamPayload, stadiumPayload, gamePayload] = await Promise.all([
      fetchJson(`${LIVE_BASE}/teams`),
      fetchJson(`${LIVE_BASE}/stadiums`),
      fetchJson(`${LIVE_BASE}/games`),
    ]);
    teams = (teamPayload.teams || teamPayload || []).map(normalizeTeam).filter((team) => Number.isFinite(team.id));
    stadiums = (stadiumPayload.stadiums || stadiumPayload || []).map(normalizeStadium).filter((stadium) => Number.isFinite(stadium.id));
    const teamById = new Map(teams.map((team) => [team.id, team]));
    const stadiumById = new Map(stadiums.map((stadium) => [stadium.id, stadium]));
    matches = (gamePayload.games || gamePayload || []).map((item) => normalizeMatch(item, teamById, stadiumById)).filter((match) => Number.isFinite(match.id));
    if (teams.length < 40 || stadiums.length < 10 || matches.length < 60) {
      throw new Error(`live API returned incomplete data: teams=${teams.length}, stadiums=${stadiums.length}, matches=${matches.length}`);
    }
  } catch (error) {
    liveError = error instanceof Error ? error.message : String(error);
    if (process.env.WORLDCUP_WRITE_FALLBACK !== "true") {
      console.log(
        JSON.stringify(
          {
            ok: true,
            mode: "fallback-current-snapshot",
            wrote: false,
            liveError,
            note: "Live API unavailable; kept existing src/worldcupData.ts unchanged.",
          },
          null,
          2,
        ),
      );
      return;
    }
    const current = await import('../src/worldcupData.ts');
    teams = current.worldCupTeams;
    stadiums = current.worldCupStadiums;
    matches = current.worldCupMatches;
  }
  const groups = [...new Set(teams.map((team) => team.group).filter(Boolean))].sort().map((group) => ({
    group,
    teams: teams.filter((team) => team.group === group).map((team) => team.fifaCode),
  }));
  const source = {
    liveApi: `${LIVE_BASE}/*`,
    updateMode: liveError ? 'fallback-current-snapshot' : 'live-api',
    liveError,
    teams: liveError ? 'existing src/worldcupData.ts' : `${LIVE_BASE}/teams`,
    groupMatches: liveError ? 'existing src/worldcupData.ts' : `${LIVE_BASE}/games`,
    stadiums: liveError ? 'existing src/worldcupData.ts' : `${LIVE_BASE}/stadiums`,
    knockout: 'openfootball/worldcup/2026--usa/cup_finals.txt fallback remains in repository snapshot',
    context: 'openfootball/worldcup/2026--usa/cup.txt',
  };
  writeFileSync('src/worldcupData.ts', tsFile({ teams, stadiums, groups, matches, source }));
  console.log(JSON.stringify({ ok: true, mode: source.updateMode, teams: teams.length, groups: groups.length, stadiums: stadiums.length, matches: matches.length, liveError }, null, 2));
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
